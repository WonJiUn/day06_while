package day06_while;

public class Quiz04_5 {
	public static void main(String[] args) {
		
		/*
		 * 이중 for문에서 i는 1부터 10까지, j는 1부터 10까지 반복시키며 *표를 출력시키면?
		 */
		
		for(int i = 1; i<=10; i++) {
			for(int j = 1; j<=10; j++) {
			System.out.print("*");
			}
			System.out.println();
		}
		
		
		
		
	}
}
